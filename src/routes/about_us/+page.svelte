        <span class="about-us-text008">The Perpetual Issue</span>
        <span class="about-us-text009"><span>The Solution</span></span>
        <span class="about-us-text011">
          <span class="about-us-text012">
            The socioeconomic and environmental challenges surrounding the fishery
            industry are multifaceted and complex.
          </span>
          <span class="about-us-text013">Let’s break it down</span>
          <span>.</span>
        </span>
        <span class="about-us-text015">
          <span>Seafood in Thailand comes from three primary sources.</span>
        </span>
        <span class="about-us-text017">
          <span class="about-us-text018">
            <span>Thai commercial fisheries operate large deep sea trawlers</span>
            <br />
            <span></span>
            <br />
            <span>
              Their nets sweep and destroy fragile, biodiverse sea floors
            </span>
            <br />
            <span></span>
            <br />
            <span>
              Sea turtles, dolphins, sharks, and juvenile fish are unintentionally
              caught and killed in the nets. Unwanted catches are known as
            </span>
          </span>
          <span>
            <span>bycatch.</span>
            <br />
            <span></span>
          </span>
          <span>
            <span></span>
            <br />
            <span>Coral reefs are suffocated and wiped out by ghost nets</span>
          </span>
        </span>
        <span class="about-us-text036">
          <span>
            <span>
              Month-long excursions are not uncommon among large trawlers.
            </span>
            <br />
            <span></span>
            <br />
            <span>
              Without equipment to freeze and store the catch, the boats resort to
              large amounts of formalin to preserve their catches.
            </span>
            <br />
            <span></span>
            <br />
            <span>
              The chemicals remain inside the fish when they arrive to consumers.
            </span>
          </span>
        </span>
        <span class="about-us-text047">
          <span>
            <span>
              Modern-day slavery is ever present amongst the Thai Fishing
              Industry.
            </span>
            <br />
            <span></span>
            <br />
            <span>
              Of the 162,800 registered fishery workers, 101,650 are foreign.
            </span>
            <br />
            <span></span>
            <br />
            <span>Human trafficking has been repeatedly documented.</span>
            <br />
            <span></span>
            <br />
            <span>
              Stories of abduction, abuse, and slavery constantly emerge.
            </span>
            <br />
            <span></span>
          </span>
        </span>
        <span class="about-us-text064">
          <span>
            <span>
              Commercial seafood processing, transportation, and distribution
              processes have been found to use migrant child labor.
            </span>
            <br />
            <span></span>
            <br />
            <span>
              At as young as 10 years old, migrant children drop out of school to
              work in the industry. More than 10% leave for work at the age of 15.
            </span>
            <br />
            <span></span>
            <br />
            <span>
              Their labor intensive jobs often require them to work for 12 hours a
              day.
            </span>
          </span>
        </span>
        <span class="about-us-text075">
          <span>
            <span>
              Threats of international seafood trade bans prompted new policies to
              tackle unlawful, unregulated, and unreported fishing
            </span>
            <br />
            <span></span>
            <br />
            <span>The rectification took immediate effect</span>
            <br />
            <span></span>
            <br />
            <span>The laws applied across all fisheries</span>
            <br />
            <span></span>
            <br />
            <span>they include</span>
            <br />
            <span></span>
            <br />
            <span>a ban on individuals under the age of 18 on fishing boats</span>
            <br />
            <span></span>
            <br />
            <span>a regulation on fishing nets used</span>
            <br />
            <span></span>
            <br />
            <span>and negligible funding</span>
            <br />
            <span></span>
            <br />
            <span></span>
            <br />
            <span>
              a crackdown on forced labor and child labor left many without
              livelihoods
            </span>
          </span>
        </span>
        <span class="about-us-text108">
          <span>
            <span>Fisherfolk make up around 80% of fisheries in Thailand</span>
            <br />
            <span></span>
            <br />
            <span>They face several underlying struggles</span>
          </span>
        </span>
        <span class="about-us-text115">
          <span>
            <span>
              Their small boats, without means of seafood preservation, force them
              to operate on a day-trip basis.
            </span>
            <br />
            <span></span>
            <br />
            <span>
              Their fresh catch have to be sold daily. Or formalin is added.
            </span>
            <br />
            <span></span>
            <br />
            <span>
              This, in combination with their low supply chain infrastructure,
              limiteds their sales to local markets and restaurants.
            </span>
            <br />
            <span></span>
            <br />
            <span>
              Thus, most lack market power and are often exploited for low prices.
            </span>
            <br />
            <span></span>
            <br />
            <span>
              Without liquid assets and access to traditional banks, many resort
              to loan sharks for finances.
            </span>
          </span>
        </span>
        <span class="about-us-text134">
          <span>
            <span>
              New fishing regulations disproportionately impact artisanal
              fisheries.
            </span>
            <br />
            <span></span>
            <br />
            <span>
              With no minors allowed on boats to combat industrial child labor,
              artisanal fishermen are unable to pass their knowledge and
              traditions to their children.
            </span>
            <br />
            <span></span>
            <br />
            <span>
              They are often left at home without their parents, and without
              parenting, face an increased likeliness of succumbing to drug use
              and gambling addictions, a widespread and culturally engrained
              issue. Thus begins intergenerational poverty.
            </span>
            <br />
            <span></span>
            <br />
            <span>
              With new guidelines on nets to tackle commercial bycatch, many
              fishermen are not able to afford new equipment and are forced into
              debt or to cease operations.
            </span>
          </span>
        </span>
